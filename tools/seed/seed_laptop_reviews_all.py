def post_review(product_id: str, user_num: int, rating: int, title: str, content: str,
                quality_rating: int, verified: bool) -> dict:
    payload = {
        "productId": product_id,
        "userId": uid(user_num),
        "rating": rating,
        "title": title,
        "content": content,
        "qualityRating": quality_rating,
        "verifiedPurchase": verified,
    }
    resp = requests.post(BASE_URL, json=payload, timeout=10)
    resp.raise_for_status()
    return resp.json()


def main():
    print("=" * 60)
    print("노트북 전체 리뷰 시드 시작")
    print("=" * 60)

    total_success = 0
    total_fail = 0
    user_offset = 500  # 기존 리뷰 user ID와 겹치지 않게 500부터 시작

    for product_id, product_name, reviews in PRODUCTS:
        print(f"\n[{product_name}] {len(reviews)}개 리뷰 추가 중...")
        success = 0
        for i, (rating, title, content, quality_rating, verified) in enumerate(reviews):
            try:
                post_review(product_id, user_offset + i, rating, title, content, quality_rating, verified)
                print(f"  ✓ [{rating}★] {title[:25]}...")
                success += 1
            except Exception as e:
                print(f"  ✗ 실패: {title[:25]}... - {e}")
                total_fail += 1
            time.sleep(0.05)
        print(f"  → {success}개 완료")
        total_success += success
        user_offset += len(reviews) + 10  # 제품별 user offset 분리

    print("\n" + "=" * 60)
    print(f"전체 완료: 성공 {total_success}개, 실패 {total_fail}개")
    print("=" * 60)

    # 결과 검증
    print("\n[검증] 등록된 리뷰 수 확인...")
    for product_id, product_name, _ in PRODUCTS:
        resp = requests.get(f"{BASE_URL}/product/{product_id}?size=1")
        total = resp.json().get("totalElements", 0)
        summary_resp = requests.get(f"{BASE_URL}/product/{product_id}/summary")
        summary = summary_resp.json()
        avg = summary.get("averageRating", "N/A")
        print(f"  {product_name}: {total}개 리뷰, 평균 {avg}★")


if __name__ == "__main__":
    main()
